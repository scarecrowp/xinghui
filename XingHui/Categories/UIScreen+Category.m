#import "UIScreen+Category.h"

@implementation UIScreen (Category)

+ (CGFloat)screenHight
{
    return [[UIScreen mainScreen]bounds].size.height;
}

+ (CGFloat)screenWidth
{
    return [[UIScreen mainScreen]bounds].size.width;
}

@end
